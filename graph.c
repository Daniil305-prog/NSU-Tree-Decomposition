#include "graph.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

// ==========================================
// 1. ФУНКЦИЯ СОЗДАНИЯ И ИНИЦИАЛИЗАЦИИ ГРАФА
// ==========================================
Graph *create_graph(int num_vertices)
{
    // Выделяем память под саму управляющую структуру графа
    Graph *g = (Graph *)malloc(sizeof(Graph));
    if (!g)
        return NULL; // Если операционная система не дала память — выходим

    g->num_vertices = num_vertices; // Записываем общее число вершин
    g->num_edges = 0;               // Пока рёбер нет, счётчик равен нулю

    // Выделяем массив под списки смежности.
    // Используем (num_vertices + 1), чтобы нумерация вершин шла удобно: от 1 до N (а не от 0)
    g->adj = (AdjList *)calloc(num_vertices + 1, sizeof(AdjList));
    if (!g->adj)
    {
        free(g); // Если память не выделилась, чистим граф и выходим
        return NULL;
    }

    // Настраиваем "мешок" для соседей каждой конкретной вершины
    for (int i = 1; i <= num_vertices; i++)
    {
        g->adj[i].degree = 0;   // Изначально у вершины 0 соседей
        g->adj[i].capacity = 4; // Выделяем стартовое место под 4 соседа (будет расширяться)

        // Выделяем память под сам динамический массив номеров соседей
        g->adj[i].neighbors = (int *)malloc(g->adj[i].capacity * sizeof(int));
    }

    return g; // Возвращаем указатель на полностью готовый граф
}

// ==========================================
// 2. ФУНКЦИЯ ДОБАВЛЕНИЯ РЕБРА (СВЯЗИ)
// ==========================================
void add_edge(Graph *g, int u, int v)
{
    // Защита: проверяем, существует ли граф и лежат ли вершины в правильном диапазоне
    if (!g || u < 1 || u > g->num_vertices || v < 1 || v > g->num_vertices)
        return;

    // --- Шаг А: Добавляем вершину 'v' в список соседей вершины 'u' ---
    // Если текущий массив соседей у 'u' заполнился (degree догнал capacity)
    if (g->adj[u].degree >= g->adj[u].capacity)
    {
        g->adj[u].capacity *= 2; // Удваиваем емкость (стратегия динамического массива)
        // Перевыделяем память большего размера
        g->adj[u].neighbors = (int *)realloc(g->adj[u].neighbors, g->adj[u].capacity * sizeof(int));
    }
    // Записываем соседа в конец массива и инкрементируем (увеличиваем) счетчик степени вершины
    g->adj[u].neighbors[g->adj[u].degree++] = v;

    // --- Шаг Б: Добавляем вершину 'u' в список соседей вершины 'v' ---
    // Делаем то же самое зеркально, так как наш граф неориентированный (связь работает в обе стороны)
    if (g->adj[v].degree >= g->adj[v].capacity)
    {
        g->adj[v].capacity *= 2;
        g->adj[v].neighbors = (int *)realloc(g->adj[v].neighbors, g->adj[v].capacity * sizeof(int));
    }
    g->adj[v].neighbors[g->adj[v].degree++] = u;
}

// ==========================================
// 3. ФУНКЦИЯ ПРОВЕРКИ СУЩЕСТВОВАНИЯ РЕБРА
// ==========================================
bool has_edge(const Graph *g, int u, int v)
{
    // Если параметры некорректны, связи точно нет
    if (!g || u < 1 || u > g->num_vertices || v < 1 || v > g->num_vertices)
        return false;

    // Бежим циклом по всем текущим соседям вершины 'u'
    for (int i = 0; i < g->adj[u].degree; i++)
    {
        // Если среди соседей нашли вершину 'v', значит ребро существует
        if (g->adj[u].neighbors[i] == v)
        {
            return true;
        }
    }
    return false; // Обошли всех соседей и ничего не нашли — ребра нет
}

// ==========================================
// 4. ФУНКЦИЯ ПОЛНОЙ ОЧИСТКИ ПАМЯТИ (БЕЗ УТЕЧЕК)
// ==========================================
void free_graph(Graph *g)
{
    if (!g)
        return; // Если чистить нечего — уходим

    if (g->adj)
    {
        // Сначала освобождаем внутренние динамические массивы соседей для каждой вершины
        for (int i = 1; i <= g->num_vertices; i++)
        {
            if (g->adj[i].neighbors)
            {
                free(g->adj[i].neighbors);
            }
        }
        free(g->adj); // Затем удаляем сам массив структур списков
    }
    free(g); // В самом конце удаляем головную структуру графа
}

// ==========================================
// 5. БЕЗОПАСНЫЙ ПАРСЕР ФАЙЛОВ ФОРМАТА DIMACS (.gr)
// ==========================================
Graph *read_graph_dimacs(const char *filename)
{
    // Открываем файл только для чтения ("r" - read)
    FILE *f = fopen(filename, "r");
    if (!f)
    {
        fprintf(stderr, "[ОШИБКА КРИТИЧЕСКАЯ]: Не удалось открыть файл '%s'.\n", filename);
        return NULL;
    }

    char line[1024]; // Буфер, куда будем построчно считывать текст из файла
    int num_vertices = 0, num_edges = 0;
    bool p_line_found = false;
    int line_counter = 0; // Счётчик строк для вывода точных ошибок парсинга

    // --- ЭТАП 1: Поиск главной строки параметров "p tw вершин рёбер" ---
    while (fgets(line, sizeof(line), f))
    {
        line_counter++;
        // Если строка начинается на 'c' (комментарий) или она пустая — пропускаем её
        if (line[0] == 'c' || line[0] == '\n' || line[0] == '\r')
            continue;

        // Наткнулись на управляющую строку спецификации формата PACE/DIMACS
        if (line[0] == 'p')
        {
            // Парсим из неё два целых числа: кол-во вершин и кол-во рёбер
            if (sscanf(line, "p tw %d %d", &num_vertices, &num_edges) != 2)
            {
                fclose(f);
                return NULL; // Ошибка формата заголовка
            }
            p_line_found = true; // Заголовок успешно найден и считан
            break;               // Выходим из цикла поиска заголовка, дальше пойдут рёбра
        }
    }

    // Если до конца файла строки "p tw" так и не нашлось
    if (!p_line_found)
    {
        fclose(f);
        return NULL;
    }

    // --- ЭТАП 2: Выделение памяти под граф ---
    // Создаем пустой граф нужного размера, используя наш конструктор выше
    Graph *g = create_graph(num_vertices);
    if (!g)
    {
        fclose(f);
        return NULL;
    }
    g->num_edges = num_edges; // Запоминаем плановое количество рёбер

    // --- ЭТАП 3: Построчное чтение и добавление рёбер ---
    int edges_read = 0;
    while (fgets(line, sizeof(line), f))
    {
        line_counter++;
        // Пропускаем строки комментариев, которые могут быть и в середине файла
        if (line[0] == 'c' || line[0] == '\n' || line[0] == '\r')
            continue;

        int u = 0, v = 0;
        // Извлекаем из строки два номера вершин (например: "1 5")
        if (sscanf(line, "%d %d", &u, &v) != 2)
            continue; // Если строка битая — игнорируем

        // ЗАЩИТА: Проверяем индексы вершин, чтобы не поймать Segmentation Fault (выход за массив)
        if (u < 1 || u > num_vertices || v < 1 || v > num_vertices)
        {
            free_graph(g); // Срочно чистим память
            fclose(f);
            return NULL; // Прерываем работу из-за некорректного файла
        }

        // Если проверки прошли — вызываем нашу функцию и связываем вершины в структуре данных
        add_edge(g, u, v);
        edges_read++; // Увеличиваем счетчик реально добавленных ребер
    }

    fclose(f); // Закрываем файл, работа с диском завершена
    return g;  // Возвращаем полностью собранный и готовый к эвристикам граф
}